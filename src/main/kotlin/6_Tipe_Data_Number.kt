fun main() {

    // Tipe data untuk number yang terdapat di dalam kotlin
    val minByte = -128
    val maxByte = 128
    val minShort = -32768
    val maxShort = 32768
    val minInt = -2147483648
    val maxInt = 2147483648
    val minLong = -9223372036854775807
    val maxLong = 9223372036854775807

